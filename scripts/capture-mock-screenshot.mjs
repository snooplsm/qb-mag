import { createServer } from "node:http";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "playwright";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const srcDir = path.join(rootDir, "src");
const outDir = path.join(rootDir, "docs", "assets");
const outQueuePath = path.join(outDir, "mock-queue.png");
const outAddPath = path.join(outDir, "mock-add.png");
const port = 4173;

const mimeByExt = new Map([
  [".html", "text/html; charset=utf-8"],
  [".js", "text/javascript; charset=utf-8"],
  [".css", "text/css; charset=utf-8"],
  [".json", "application/json; charset=utf-8"],
  [".png", "image/png"],
  [".jpg", "image/jpeg"],
  [".jpeg", "image/jpeg"],
  [".svg", "image/svg+xml"]
]);

function resolvePath(urlPathname) {
  const clean = urlPathname.split("?")[0].split("#")[0];
  const rel = clean === "/" ? "/index.html" : clean;
  const full = path.normalize(path.join(srcDir, rel));
  if (!full.startsWith(srcDir)) return null;
  return full;
}

const server = createServer(async (req, res) => {
  try {
    const reqUrl = new URL(req.url || "/", `http://127.0.0.1:${port}`);
    const fullPath = resolvePath(reqUrl.pathname);
    if (!fullPath) {
      res.statusCode = 403;
      res.end("Forbidden");
      return;
    }
    const body = await readFile(fullPath);
    const ext = path.extname(fullPath).toLowerCase();
    res.setHeader("content-type", mimeByExt.get(ext) || "application/octet-stream");
    res.statusCode = 200;
    res.end(body);
  } catch {
    res.statusCode = 404;
    res.end("Not Found");
  }
});

await new Promise((resolve) => server.listen(port, "127.0.0.1", resolve));

try {
  const browser = await chromium.launch({ headless: true });
  const page = await browser.newPage({ viewport: { width: 870, height: 640 } });
  await page.goto(`http://127.0.0.1:${port}/index.html?mock=1&screen=queue`, { waitUntil: "networkidle" });
  await page.waitForSelector(".queue-card", { timeout: 15_000 });
  await mkdir(outDir, { recursive: true });
  await page.screenshot({ path: outQueuePath, fullPage: false });

  await page.goto(`http://127.0.0.1:${port}/index.html?mock=1&screen=add`, { waitUntil: "networkidle" });
  await page.waitForSelector("#addPanel:not(.hidden) .add-preview-card", { timeout: 15_000 });
  await page.screenshot({ path: outAddPath, fullPage: false });
  await browser.close();
  await writeFile(
    path.join(outDir, "README.txt"),
    "Generated by: npm run docs:screenshot\nFiles: mock-queue.png, mock-add.png\nDo not edit manually.\n",
    "utf8"
  );
  console.log(`Saved screenshots: ${outQueuePath}, ${outAddPath}`);
} finally {
  await new Promise((resolve) => server.close(resolve));
}
